SET NAMES utf8mb4;
SET time_zone = '+00:00';

CREATE TABLE IF NOT EXISTS usuarios_escopos (
    id BIGINT UNSIGNED AUTO_INCREMENT PRIMARY KEY,
    usuario_id BIGINT UNSIGNED NOT NULL,
    conta_id BIGINT UNSIGNED NOT NULL,
    orgao_id BIGINT UNSIGNED NULL,
    escopo_acesso ENUM(
        'PROPRIA_UNIDADE',
        'PROPRIO_ORGAO',
        'MUNICIPAL',
        'REGIONAL',
        'ESTADUAL',
        'MULTIINSTITUCIONAL',
        'GLOBAL'
    ) NOT NULL,
    status_escopo ENUM('ATIVO', 'INATIVO') NOT NULL DEFAULT 'ATIVO',
    created_at DATETIME NOT NULL DEFAULT CURRENT_TIMESTAMP,
    updated_at DATETIME NOT NULL DEFAULT CURRENT_TIMESTAMP ON UPDATE CURRENT_TIMESTAMP,
    CONSTRAINT fk_usuarios_escopos_usuario FOREIGN KEY (usuario_id) REFERENCES usuarios(id),
    CONSTRAINT fk_usuarios_escopos_conta FOREIGN KEY (conta_id) REFERENCES contas(id),
    CONSTRAINT fk_usuarios_escopos_orgao FOREIGN KEY (orgao_id) REFERENCES orgaos(id),
    UNIQUE KEY uk_usuarios_escopos_usuario_escopo (usuario_id, escopo_acesso),
    KEY idx_usuarios_escopos_contexto (conta_id, orgao_id, status_escopo)
) ENGINE=InnoDB DEFAULT CHARSET=utf8mb4 COLLATE=utf8mb4_unicode_ci;

CREATE TABLE IF NOT EXISTS incidentes (
    id BIGINT UNSIGNED AUTO_INCREMENT PRIMARY KEY,
    conta_id BIGINT UNSIGNED NOT NULL,
    orgao_id BIGINT UNSIGNED NOT NULL,
    unidade_id BIGINT UNSIGNED NULL,
    numero_ocorrencia VARCHAR(60) NOT NULL,
    nome_incidente VARCHAR(180) NOT NULL,
    tipo_ocorrencia VARCHAR(100) NOT NULL,
    classificacao_inicial VARCHAR(60) NULL,
    data_hora_acionamento DATETIME NULL,
    data_hora_abertura DATETIME NOT NULL,
    municipio VARCHAR(120) NULL,
    local_detalhado VARCHAR(255) NULL,
    coordenadas VARCHAR(120) NULL,
    orgao_primeira_informacao VARCHAR(180) NULL,
    canal_recebimento VARCHAR(80) NULL,
    comunicante VARCHAR(180) NULL,
    descricao_inicial TEXT NOT NULL,
    situacao_inicial_observada TEXT NULL,
    populacao_potencialmente_afetada INT UNSIGNED NULL,
    danos_humanos_iniciais TEXT NULL,
    danos_materiais_iniciais TEXT NULL,
    danos_ambientais_iniciais TEXT NULL,
    riscos_imediatos TEXT NULL,
    orgao_lider_inicial VARCHAR(180) NULL,
    status_incidente ENUM('ABERTO', 'EM_ANDAMENTO', 'CONTROLADO', 'ENCERRADO') NOT NULL DEFAULT 'ABERTO',
    plancon_id BIGINT UNSIGNED NULL,
    cenario_id BIGINT UNSIGNED NULL,
    aberto_por_usuario_id BIGINT UNSIGNED NOT NULL,
    encerrado_por_usuario_id BIGINT UNSIGNED NULL,
    created_at DATETIME NOT NULL DEFAULT CURRENT_TIMESTAMP,
    updated_at DATETIME NOT NULL DEFAULT CURRENT_TIMESTAMP ON UPDATE CURRENT_TIMESTAMP,
    CONSTRAINT fk_incidentes_conta FOREIGN KEY (conta_id) REFERENCES contas(id),
    CONSTRAINT fk_incidentes_orgao FOREIGN KEY (orgao_id) REFERENCES orgaos(id),
    CONSTRAINT fk_incidentes_unidade FOREIGN KEY (unidade_id) REFERENCES unidades(id),
    CONSTRAINT fk_incidentes_usuario_abertura FOREIGN KEY (aberto_por_usuario_id) REFERENCES usuarios(id),
    CONSTRAINT fk_incidentes_usuario_encerramento FOREIGN KEY (encerrado_por_usuario_id) REFERENCES usuarios(id),
    UNIQUE KEY uk_incidentes_orgao_numero (orgao_id, numero_ocorrencia),
    KEY idx_incidentes_escopo_status (conta_id, orgao_id, unidade_id, status_incidente),
    KEY idx_incidentes_data_abertura (data_hora_abertura),
    KEY idx_incidentes_plancon (plancon_id)
) ENGINE=InnoDB DEFAULT CHARSET=utf8mb4 COLLATE=utf8mb4_unicode_ci;

