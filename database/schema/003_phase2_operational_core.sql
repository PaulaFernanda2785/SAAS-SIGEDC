    encaminhamento TEXT NULL,
    status_registro ENUM('ABERTO', 'EM_ANDAMENTO', 'CONCLUIDO') NOT NULL DEFAULT 'ABERTO',
    criticidade ENUM('BAIXA', 'MODERADA', 'ALTA', 'CRITICA') NOT NULL DEFAULT 'MODERADA',
