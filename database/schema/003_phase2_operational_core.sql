    CONSTRAINT fk_incidentes_registros_orgao FOREIGN KEY (orgao_id) REFERENCES orgaos(id),
    CONSTRAINT fk_incidentes_registros_unidade FOREIGN KEY (unidade_id) REFERENCES unidades(id),
    CONSTRAINT fk_incidentes_registros_usuario FOREIGN KEY (registrado_por_usuario_id) REFERENCES usuarios(id),
    KEY idx_incidentes_registros_escopo (conta_id, orgao_id, incidente_id, data_hora_registro),
    KEY idx_incidentes_registros_tipo (tipo_registro, status_registro),
    KEY idx_incidentes_registros_periodo (periodo_operacional_id)
) ENGINE=InnoDB DEFAULT CHARSET=utf8mb4 COLLATE=utf8mb4_unicode_ci;
