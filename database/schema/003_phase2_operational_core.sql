CREATE TABLE IF NOT EXISTS incidentes_registros_operacionais (
    id BIGINT UNSIGNED AUTO_INCREMENT PRIMARY KEY,
    incidente_id BIGINT UNSIGNED NOT NULL,
    periodo_operacional_id BIGINT UNSIGNED NULL,
    conta_id BIGINT UNSIGNED NOT NULL,
    orgao_id BIGINT UNSIGNED NOT NULL,
    unidade_id BIGINT UNSIGNED NULL,
    data_hora_registro DATETIME NOT NULL,
    tipo_registro ENUM('DECISAO', 'ACIONAMENTO', 'OCORRENCIA', 'MOBILIZACAO', 'ATUALIZACAO', 'COMUNICADO') NOT NULL,
    titulo_registro VARCHAR(180) NOT NULL,
    descricao_registro TEXT NOT NULL,
